    fun awardXP(amount: Int) {
        viewModelScope.launch {
            // SECURITY: Anti-Cheat checks
            if (amount > 500) {
                Log.e("SecurityGuard", "Fraudulent XP gain detected! Prevented.")
                return@launch
            }

            val user = repository.getUserSync() ?: return@launch
            val newXp = user.xp + amount
            val potentialLevel = (newXp / 100) + 1
            
            val finalLevel = if (potentialLevel > 100) 100 else potentialLevel
            val levelUp = finalLevel > user.level

            val updatedUser = user.copy(
                xp = newXp,
                level = finalLevel
            )
            repository.insertOrUpdateUser(updatedUser)

            if (levelUp) {
                celebrationTriggered.value = true
                speak("Congratulations! You leveled up to level $finalLevel!")
            }
        }
    }

    fun subtractXp(amount: Int) {
        viewModelScope.launch {
            val user = repository.getUserSync() ?: return@launch
            var newXp = user.xp - amount
            if (newXp < 0) newXp = 0
            val newLevel = (newXp / 100) + 1
            repository.insertOrUpdateUser(user.copy(xp = newXp, level = newLevel))
        }
    }

    // --- Admin panel actions ---
    fun checkAdminLogin(pass: String): Boolean {
        return pass == adminPassword.value
    }

    fun processPremiumRequest(requestId: Long, approve: Boolean) {
        viewModelScope.launch {
            val requests = repository.getAllRequestsSync()
            val req = requests.find { it.id == requestId } ?: return@launch
            val updatedStatus = if (approve) "APPROVED" else "REJECTED"
            repository.updateRequest(req.copy(status = updatedStatus))

            if (approve) {
                val cleanUsername = req.username.trim().removePrefix("@")
                val allUsers = repository.getAllUsers()
                val targetUser = allUsers.find { it.username.trim().removePrefix("@").equals(cleanUsername, ignoreCase = true) }
                if (targetUser != null) {
                    repository.insertOrUpdateUser(targetUser.copy(isPremium = true))
                    awardXP(100)
                } else {
                    val curUser = repository.getUserSync()
                    if (curUser != null) {
                        repository.insertOrUpdateUser(curUser.copy(isPremium = true))
                        awardXP(100)
                    }
                }
            }
        }
    }

    fun updateUsersPremiumManual(username: String, grandPremium: Boolean) {
        viewModelScope.launch {
            val cleanUsername = username.trim().removePrefix("@")
            val all = repository.getAllUsers()
            val target = all.find { it.username.trim().removePrefix("@").equals(cleanUsername, ignoreCase = true) }
            if (target != null) {
                repository.insertOrUpdateUser(target.copy(isPremium = grandPremium))
            } else {
                val curUser = repository.getUserSync()
                if (curUser != null) {
                    repository.insertOrUpdateUser(curUser.copy(isPremium = grandPremium))
                }
            }
        }
    }

    fun changeUserXpManual(username: String, changeAmount: Int) {
        viewModelScope.launch {
            val cleanUsername = username.trim().removePrefix("@")
            val all = repository.getAllUsers()
            val target = all.find { it.username.trim().removePrefix("@").equals(cleanUsername, ignoreCase = true) }
            if (target != null) {
                val newXp = (target.xp + changeAmount).coerceAtLeast(0)
                val newLevel = (newXp / 100) + 1
                repository.insertOrUpdateUser(target.copy(xp = newXp, level = newLevel))
            } else {
                val curUser = repository.getUserSync()
                if (curUser != null) {
                    val newXp = (curUser.xp + changeAmount).coerceAtLeast(0)
                    val newLevel = (newXp / 100) + 1
                    repository.insertOrUpdateUser(curUser.copy(xp = newXp, level = newLevel))
                }
            }
        }
    }

    fun updateSocialSettings(tg: String, insta: String) {
        viewModelScope.launch {
            val current = repository.getSettingsSync() ?: AppSettingsEntity()
            repository.saveSettings(current.copy(telegramLink = tg, instagramLink = insta))
        }
    }

    fun updateAdminCode(newCode: String) {
        viewModelScope.launch {
            val current = repository.getSettingsSync() ?: AppSettingsEntity()
            repository.saveSettings(current.copy(adminCode = newCode))
            adminPassword.value = newCode
        }
    }

    // --- Certificates Issuance ---
    fun generateUserCertificate(userName: String, certLevel: String) {
        viewModelScope.launch {
            val cleanDate = SimpleDateFormat("dd.MM.yyyy", Locale.getDefault()).format(Date())
            val id = "EAM-${(1000..9999).random()}-${(10..99).random()}"
            val qrContent = "https://eam-verify-applet.web.app/cert?id=$id&user=$userName"
            val cert = CertificateEntity(
                id = id,
                levelCode = certLevel,
                userName = userName,
                issueDate = cleanDate,
                qrCodePayload = qrContent
            )
            repository.insertCertificate(cert)
            awardXP(150)
        }
    }

    fun revokeCertificate(certId: String) {
        viewModelScope.launch {
            repository.deleteCertificateById(certId)
        }
    }

    // --- Premium user purchase requests ---
    fun uploadPremiumCheck(tariffName: String, receiptBase64: String) {
        viewModelScope.launch {
            val user = userFlow.value ?: return@launch
            val request = PremiumRequestEntity(
                username = user.username,
                tariff = tariffName,
                receiptImageUri = receiptBase64,
                status = "PENDING"
            )
            repository.insertRequest(request)
        }
    }

