    // --- Streak calculation ---
    private suspend fun syncUserStreak() {
        val user = repository.getUserSync() ?: return
        val today = SimpleDateFormat("yyyy-MM-dd", Locale.getDefault()).format(Date())
        if (user.lastActiveDate.isEmpty()) {
            val updated = user.copy(lastActiveDate = today, streak = 1)
            repository.insertOrUpdateUser(updated)
            return
        }

        if (user.lastActiveDate == today) {
            // Already logged in today
            return
        }

        val sdf = SimpleDateFormat("yyyy-MM-dd", Locale.getDefault())
        try {
            val lastDate = sdf.parse(user.lastActiveDate)
            val currentDate = sdf.parse(today)
            val diff = currentDate.time - lastDate.time
            val diffDays = diff / (24 * 60 * 60 * 1000)

            val updatedStreak = when {
                diffDays == 1L -> user.streak + 1  // consecutive day
                diffDays > 1L -> 1                 // streak broken, reset to 1
                else -> user.streak
            }

            val updatedUser = user.copy(lastActiveDate = today, streak = updatedStreak)
            repository.insertOrUpdateUser(updatedUser)
        } catch (e: Exception) {
            Log.e("StreakSync", "Failed to sync streak", e)
        }
    }

