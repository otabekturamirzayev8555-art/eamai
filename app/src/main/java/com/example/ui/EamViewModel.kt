            writingFeedback.value = "Essay kamida 20 ta so'zdan iborat bo'lishi kerak."
            return
        }

        viewModelScope.launch {
            isAiLoading.value = true
            val prompt = """
                Foydalanuvchi yozgan Essayni tekshiring:
                "$essay"
                Menga quyidagi shakldagi JSON formatda qaytaring (hech qanday qo'shimcha matn va ```json va ``` belgilari keremas, faqat toza JSON formatida yuboring):
                {
                   "band": "IELTS 6.5",
                   "errors": "Xatolar ro'yxati (o'zbek tilida)",
                   "corrected": "To'g'rilangan variant",
                   "tips": "Qanday oshirish kerakligi maslahatlar"
                }
            """.trimIndent()

            val result = GeminiClient.generateResponse(prompt)
            try {
                // Parse feedback from generated raw string or clean regex
                val cleanedJson = result.replace("```json", "").replace("```", "").trim()
                val json = JSONObject(cleanedJson)
                writingBandScore.value = json.optString("band", "IELTS 5.5")
                writingFeedback.value = """
                    ❌ Xatolar va Kamchiliklar:
                    ${json.optString("errors", "Muammolar topilmadi.")}
                    
                    ✨ To'g'ri variant:
                    ${json.optString("corrected")}
                    
                    💡 Tavsiyalar:
                    ${json.optString("tips")}
                """.trimIndent()
                
                // reward User for task compilation!
                if (!isWritingCompleted.value) {
                    awardXP(60)
                    isWritingCompleted.value = true
                }
            } catch (e: Exception) {
                // Fallback direct parser if AI returned normal text instead of JSON
                writingBandScore.value = "IELTS 6.0"
                writingFeedback.value = result
                if (!isWritingCompleted.value) {
                    awardXP(60)
                    isWritingCompleted.value = true
                }
            }
            isAiLoading.value = false
        }
    }

